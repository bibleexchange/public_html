> ILLUSTRATION: When I'm tending garden, I don't just dump seeds or just throw seeds. Some seeds need to be placed a little below the surface while others may need to be six inches deep. There is a proper way to plant seeds.

<img src="https://www.planetnatural.com/wp-content/uploads/2013/03/healthy-sunflower-seedlings.jpg" ALIGN="RIGHT"/>

>Some folks are like seedlings. They have been growing for some time and the ministry they presently need is to help them get rightfully planted with care. Water the seed, ensure the right amount of sunlight, keep the bugs away, take out the weeds, etc.

If there is so much effort required in tending a natural seed and plant, how much more care is needed when guarding and tending a soul! Keep the weeds out of our garden.

