## Who is Sufficient for these things?

> 2 Corinthians 2:14-17 — 14 Now thanks be unto God, which always causeth us to triumph in Christ, and maketh manifest the savour of his knowledge by us in every place.  15 For we are unto God a sweet savour of Christ, in them that are saved, and in them that perish:  16 To the one we are the savour of death unto death; and to the other the savour of life unto life. And who is sufficient for these things?  17 For we are not as many, which corrupt the word of God: but as of sincerity, but as of God, in the sight of God speak we in Christ.

> v. 16 To the one we are the savour of death unto death; and to the other the savour of life unto life. And who is sufficient for these things?

> 2 Corinthians 4:12 — So then death worketh in us, but life in you.

One thing is said, two people hear leaving with two different opinions.

By the same one thing:
- 1 person helped
- Another person hurt

## We Would Have EVERYONE to Believe and UNDERSTAND Christ

Here is a confusing result. We want to help everyone all the time, but that isn't always the case.

> 2 Cor. 2:14 — ... maketh manifest the savour of his knowledge by us in every place.

God wants to make some things known by you.

God wants his Word in us so that we speak His Word. So that people will have an understanding of who God is by His Word that is made manifest in you by God.

- I thank God that He speaks to me His Word. 
- I thank God that He is changing me with His Word. 
- God is developing my faith with His Word

## Say What God Says

Saying what God says may "get you into trouble" , but it will also keep you in the time of trouble.

When you say what God says, Some may think, "You are killing me."

> RESPONSE: God's intention is to kill flesh — the carnal part of our mind. (Romans 8:13). His purpose is not ruin or destroy us, but to deliver us from death and sin.

Some will hear the message of mortification and because they love there self and their sin they leave with the opinion, "You just have a message of condemnation. You are killing me."

- What is really killing them is sin.
- If the Word kills anything, it will be the flesh.

God's Word gives life to the Spirit and soul, but it will kill carnality. We need the Word of God in our heart.

## It Will Help Them

It is going to be Word of God in your heart and sown in others that is going to help them. It is going to hurt some and help others.

The truth is, the Word of God sown would HELP **EVERYONE** if they would obey the truth.

## When They Say Its Hurting

When some respond to our witness of the Word as if they have been victimized, what should our response be?

- DON'T: Take the the attitude, "It's your fault. You just don't want to hear it."
- DO: Leave praying that God will sharpen me. "Lord help me to know better how to speak your Word how You want it said. Help me to put that Word properly in a person's heart."

## The Proper Way to Plant

